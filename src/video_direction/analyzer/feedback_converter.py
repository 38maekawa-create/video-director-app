def classify_feedback_category(text: str) -> str:
    """FBテキストからカテゴリを推定する

    Args:
        text: 音声FBの生テキスト

    Returns:
        カテゴリ文字列（7カテゴリ + "general"）
    """
    text_lower = text.lower()
    scores = {}
    for cat, words in CATEGORY_KEYWORDS.items():
        # 完全一致だけでなく部分一致もスコアリング
        scores[cat] = sum(1 for w in words if w.lower() in text_lower)

    if max(scores.values(), default=0) > 0:
        return max(scores, key=scores.get)
    return "general"


def build_system_prompt(category: str = None) -> str:
    """FB変換用のシステムプロンプトを構築する

    Args:
        category: 検出されたカテゴリ（Noneの場合は全カテゴリの概要を含む）

    Returns:
        システムプロンプト文字列
    """
    base = """あなたはプロフェッショナルな映像ディレクターです。
映像制作の現場で15年以上の経験を持ち、カラーグレーディング、編集テンポ、テロップデザイン、
音響設計のすべてに精通しています。

あなたの仕事は、映像ディレクター（なおとさん）がカジュアルに口頭で伝えた音声フィードバックを、
映像編集者が即座に実行できる、具体的・技術的・プロフェッショナルなディレクション指示に変換することです。

## 変換の原則

1. **具体的な数値・技術用語を使う**: 「もうちょっと明るく」→「露出を+0.3EV、リフトを5%持ち上げ」
2. **理由と意図を添える**: なぜその変更が必要か、仕上がりのイメージを言語化する
3. **優先度を明確にする**: 映像全体のクオリティに直結するものをhigh、微調整をlowに
4. **実行可能な形に分解する**: 1つの指示で1つのアクション。複合的なFBは複数項目に分ける
5. **ポジティブなトーン**: 指摘ではなく「こうするともっと良くなる」という改善提案として書く
6. **タイムスタンプの推定**: FBの文脈から該当箇所のタイムスタンプを可能な限り推定する"""

    # カテゴリ固有の専門知識を注入
    if category and category in CATEGORY_TEMPLATES:
        tmpl = CATEGORY_TEMPLATES[category]
        base += f"""

## 今回のFBカテゴリ: {tmpl['display_name']}

### 使用すべき専門用語:
{chr(10).join('- ' + t for t in tmpl['technical_terms'])}

### 変換ガイドライン:
{tmpl['conversion_guide']}"""
    else:
        # カテゴリ不明の場合は全カテゴリのサマリーを提示
        base += "\n\n## 変換時に参照する専門領域:\n"
        for cat_key, tmpl in CATEGORY_TEMPLATES.items():
            base += f"- **{tmpl['display_name']}**: {tmpl['description']}\n"

    return base


def build_conversion_prompt(
    raw_text: str,
    category: str = None,
    learned_rules_text: str = "",
    tracking_refs_text: str = "",
) -> str:
    """FB変換用のユーザープロンプトを構築する

    Args:
        raw_text: 音声FBの生テキスト
        category: 検出されたカテゴリ（Noneは自動検出）
        learned_rules_text: FB学習ルールのテキスト（強化版用）
        tracking_refs_text: トラッキング映像参考のテキスト（強化版用）

    Returns:
        ユーザープロンプト文字列
    """
    if category is None:
        category = classify_feedback_category(raw_text)

    prompt = f"""以下の音声フィードバックを、映像編集者向けのプロフェッショナルなディレクション指示に変換してください。
{learned_rules_text}{tracking_refs_text}

## 音声テキスト（原文ママ）:
{raw_text}

## 変換時の注意:
- 口語表現をそのまま残さず、全て専門的・具体的な指示に変換する
- 「なんか」「ちょっと」「もうちょい」等の曖昧表現は具体的な数値・方向性に置き換える
- 1つのFBに複数の指摘が含まれる場合は、structured_items に分けて個別の指示にする
- 変換後のテキスト（converted_text）は、編集者がそのまま作業指示書として使える品質にする"""

    # カテゴリ固有の出力サンプル
    if category and category in CATEGORY_TEMPLATES:
        tmpl = CATEGORY_TEMPLATES[category]
        prompt += f"""
- このFBは「{tmpl['display_name']}」カテゴリに分類されたため、{tmpl['display_name']}の専門用語を積極的に使用すること"""

    prompt += """

## 出力JSON形式:
{
  "converted_text": "変換後のプロフェッショナルなディレクション指示テキスト（複数行可。セクション見出し付きで構造化）",
  "category": "検出されたカテゴリ（cutting/color/telop/bgm/camera/composition/tempo/general）",
  "structured_items": [
    {
      "id": "1",
      "timestamp": "該当タイムスタンプ（推定。不明なら '全般'）",
      "element": "対象要素（テロップ/カット/BGM/カメラ/カラー/構図/テンポ等）",
      "instruction": "具体的かつ技術的な指示（数値・専門用語を含む）",
      "priority": "high/medium/low",
      "reason": "この変更が必要な理由（編集者の理解を助ける）",
      "reference_url": "参考映像のURL（あれば。なければnull）",
      "reference_note": "参考映像の該当箇所の説明（あれば。なければnull）"
    }
  ]
}"""

    return prompt


